"""Generate the 11 MBA-replication experiment configs.

The experiment matrix is the table from the MBA Cap. 4
``Matriz Geral dos Resultados dos Experimentos`` plus the variations
described in §"Resultado Experimento N":

| #  | Loss          | Optimizer | Scheduler                   | Dropout | Epochs | Subset       |
|----|---------------|-----------|-----------------------------|---------|--------|--------------|
| 1  | CrossEntropy  | SGD       | OneCycleLR                  | 0.2     | 25     | balanced     |
| 2  | ArcFace       | SGD       | OneCycleLR                  | 0.2     | 25     | balanced     |
| 3  | CrossEntropy  | AdamW     | OneCycleLR                  | 0.2     | 25     | balanced     |
| 4  | ArcFace       | AdamW     | OneCycleLR                  | 0.2     | 25     | balanced     |
| 5  | CrossEntropy  | AdamW     | CosineAnnealingWarmRestarts | 0.2     | 25     | balanced     |
| 6  | ArcFace       | AdamW     | CosineAnnealingWarmRestarts | 0.2     | 25     | balanced     |
| 7  | CrossEntropy  | AdamW     | OneCycleLR                  | 0.2     | 25     | Black+White  |
| 8  | ArcFace       | AdamW     | OneCycleLR                  | 0.2     | 25     | Black+White  |
| 9  | CrossEntropy  | AdamW     | OneCycleLR                  | 0.5     | 25     | balanced     |
| 10 | ArcFace       | AdamW     | OneCycleLR                  | 0.5     | 25     | balanced     |
| 11 | CrossEntropy  | AdamW     | OneCycleLR                  | 0.2     | 40     | balanced     |

Re-run this script after changing any hyperparameter to keep the
generated YAMLs in sync. The dissertation cites these configs by their
filename, so do not rename existing ones lightly.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path

CONFIGS_DIR = Path(__file__).resolve().parents[1] / "configs" / "experiments"


@dataclass
class ExpSpec:
    num: int
    loss_function: str
    optimizer: str
    scheduler: str
    dropout: float = 0.2
    num_epochs: int = 25
    class_filter: list[str] | None = None
    notes: str = ""

    def head(self) -> str:
        # An ArcFace loss only does its job when the model head produces
        # margin-corrected logits — i.e. with head="arcface".
        return "arcface" if self.loss_function == "arcface" else "linear"

    @property
    def filename(self) -> str:
        tags = [f"exp{self.num:02d}"]
        tags.append("ce" if self.loss_function == "cross_entropy" else "arcface")
        tags.append(self.optimizer)
        tags.append("onecycle" if self.scheduler == "onecyclelr" else "cosine")
        if self.class_filter:
            tags.append("blackwhite")
        if self.dropout >= 0.5:
            tags.append("dropout05")
        if self.num_epochs != 25:
            tags.append(f"{self.num_epochs}ep")
        return "_".join(tags) + ".yaml"


EXPERIMENTS: list[ExpSpec] = [
    ExpSpec(1, "cross_entropy", "sgd", "onecyclelr", notes="MBA baseline"),
    ExpSpec(2, "arcface", "sgd", "onecyclelr"),
    ExpSpec(3, "cross_entropy", "adamw", "onecyclelr"),
    ExpSpec(4, "arcface", "adamw", "onecyclelr"),
    ExpSpec(5, "cross_entropy", "adamw", "cosineannealingwarmrestarts"),
    ExpSpec(6, "arcface", "adamw", "cosineannealingwarmrestarts", notes="MBA reported zeros"),
    ExpSpec(7, "cross_entropy", "adamw", "onecyclelr", class_filter=["Black", "White"]),
    ExpSpec(8, "arcface", "adamw", "onecyclelr", class_filter=["Black", "White"]),
    ExpSpec(9, "cross_entropy", "adamw", "onecyclelr", dropout=0.5),
    ExpSpec(10, "arcface", "adamw", "onecyclelr", dropout=0.5),
    ExpSpec(11, "cross_entropy", "adamw", "onecyclelr", num_epochs=40),
]


YAML_TEMPLATE = """\
# {header}
#
# MBA Cap. 4 spec: loss={loss}, optimizer={optimizer}, scheduler={scheduler},
# dropout={dropout}, num_epochs={num_epochs}{filter_note}{notes_note}.
#
# Generated by scripts/generate_experiment_configs.py — re-run that script
# instead of editing this file by hand.

model:
  name: "LResNet50E_IR"
  pretrained: true
  num_classes: {num_classes}
  dropout: {dropout}
  head: "{head}"
  arcface_s: 30.0
  arcface_m: 0.5
  arcface_easy_margin: false

training:
  batch_size: 128
  learning_rate: 0.001
  num_epochs: {num_epochs}
  optimizer: "{optimizer}"
  scheduler: "{scheduler}"
  loss_function: "{loss}"
  test_size: 0.2
  num_workers: 4
  random_state: 42

image:
  image_size: [224, 224]
  image_mean: [0.485, 0.456, 0.406]
  image_std:  [0.229, 0.224, 0.225]

preprocessing:
  processing_type: "detect_adjust_image"
  num_workers: 4
  rotate_angle: 45
  num_rotations: 8
  max_faces_per_image: 1

data:
  dataset_file: "data/raw/fairface/fairface_labels.csv"
  dataset_image_input_path:  "data/processed/fairface_aligned"
  dataset_image_output_path: "data/processed/fairface_aligned"
  balance: "undersample"
{class_filter_block}
bucket:
  function: "download"
  bucket_client: "s3"
  bucket_path_file: "data/raw/bucket/"
  object_file_name: "fairface_original_images.zip"
  bucket_name: "dataset-fairface"

logging:
  log_dir: "outputs/logs"
  log_level: "INFO"
  format: "%(asctime)s [%(run_id)s] %(name)s %(levelname)s: %(message)s"
  log_bucket_file: "bucket.log"
  log_preprocessing_file: "preprocessing.log"
  log_face_data_file: "face_data.log"
  log_version_file: "version.log"
  log_training_file: "training.log"
  log_evaluation_file: "evaluation.log"
"""


def render(exp: ExpSpec) -> str:
    if exp.class_filter:
        items = "\n".join(f'    - "{c}"' for c in exp.class_filter)
        class_filter_block = f"  class_filter:\n{items}\n"
        num_classes = len(exp.class_filter)
        filter_note = f", classes={exp.class_filter}"
    else:
        class_filter_block = ""
        num_classes = 7
        filter_note = ""

    notes_note = f" — {exp.notes}" if exp.notes else ""
    header = f"Experiment {exp.num} — replicate MBA Cap. 4 §Resultado Experimento {exp.num}"

    return YAML_TEMPLATE.format(
        header=header,
        loss=exp.loss_function,
        optimizer=exp.optimizer,
        scheduler=exp.scheduler,
        dropout=exp.dropout,
        num_epochs=exp.num_epochs,
        num_classes=num_classes,
        head=exp.head(),
        class_filter_block=class_filter_block,
        filter_note=filter_note,
        notes_note=notes_note,
    )


def main() -> None:
    CONFIGS_DIR.mkdir(parents=True, exist_ok=True)
    for exp in EXPERIMENTS:
        out = CONFIGS_DIR / exp.filename
        out.write_text(render(exp), encoding="utf-8")
        print(f"  wrote {out.relative_to(CONFIGS_DIR.parents[1])}")


if __name__ == "__main__":
    main()
