"""Generate the Dataset-factor decomposition configs (3 seeds, matched).

The dataset factor of the causal-attribution thesis is isolated by
comparing the SAME recipe on the original vs the cleaned dataset, under
an IDENTICAL environment (torch 2.12, PIL decode, fp32, post-wins code).

Standing methodological rule (user, 2026-05-15): every scientific
experiment uses 3 seeds (42, 1, 2) with a matched comparison basis and
mean +/- std reporting. Single-seed comparisons are not defensible —
Phase 4 showed seed variance (dIR ~ 0.168) dominates factor effects
(dIR ~ 0.068).

This emits a fully matched 12-config batch:

    arm        recipe   dataset            seeds
    r1ctrl     Exp5 CE  original 97k       42, 1, 2
    r1ctrl     Exp6 AF  original 97k       42, 1, 2
    r2base     Exp5 CE  clean 72k          42, 1, 2
    r2base     Exp6 AF  clean 72k          42, 1, 2

The whole batch runs in one orchestrated job so both arms are produced
under provably identical conditions (no cross-session env drift).
"""

from __future__ import annotations

from pathlib import Path

import yaml

OUT_DIR = Path("configs/experiments_dataset_factor")
SEEDS = [42, 1, 2]
ORIGINAL_CSV = "data/raw/fairface/fairface_labels.csv"
CLEAN_CSV = "data/raw/fairface/fairface_labels_clean.csv"

RECIPES = {
    "exp05_ce": {"head": "linear", "loss_function": "cross_entropy"},
    "exp06_af": {"head": "arcface", "loss_function": "arcface"},
}
ARMS = {
    "r1ctrl": ORIGINAL_CSV,   # original dataset, current env
    "r2base": CLEAN_CSV,      # cleaned dataset, current env
}


def _cfg(head: str, loss: str, dataset_file: str, seed: int) -> dict:
    return {
        "model": {
            "name": "LResNet50E_IR",
            "pretrained": True,
            "num_classes": 7,
            "dropout": 0.2,
            "head": head,
            "arcface_s": 30.0,
            "arcface_m": 0.5,
            "arcface_easy_margin": False,
        },
        "training": {
            "batch_size": 128,
            "learning_rate": 0.001,
            "num_epochs": 25,
            "optimizer": "adamw",
            "scheduler": "cosineannealingwarmrestarts",
            "loss_function": loss,
            "test_size": 0.1,
            "val_size": 0.1,
            "num_workers": 0,  # Windows deadlock-proof: persistent_workers
            "random_state": seed,
            "grad_clip_norm": 5.0,
            "early_stopping_patience": 5,
            "use_amp": False,  # fp32 — matched across both arms
        },
        "image": {
            "image_size": [224, 224],
            "image_mean": [0.485, 0.456, 0.406],
            "image_std": [0.229, 0.224, 0.225],
        },
        "preprocessing": {
            "processing_type": "detect_adjust_image",
            "num_workers": 4,  # preprocess step not run here; schema requires >=1
            "rotate_angle": 45,
            "num_rotations": 8,
            "max_faces_per_image": 1,
        },
        "data": {
            "dataset_file": dataset_file,
            "dataset_image_input_path": "data/processed/fairface_aligned",
            "dataset_image_output_path": "data/processed/fairface_aligned",
            "balance": "undersample",
        },
        "bucket": {
            "function": "download",
            "bucket_client": "s3",
            "bucket_path_file": "data/raw/bucket/",
            "object_file_name": "fairface_original_images.zip",
            "bucket_name": "dataset-fairface",
        },
        "logging": {
            "log_dir": "outputs/logs",
            "log_level": "INFO",
            "format": "%(asctime)s [%(run_id)s] %(name)s %(levelname)s: %(message)s",
            "log_bucket_file": "bucket.log",
            "log_preprocessing_file": "preprocessing.log",
            "log_face_data_file": "face_data.log",
            "log_version_file": "version.log",
            "log_training_file": "training.log",
            "log_evaluation_file": "evaluation.log",
        },
    }


def main() -> int:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    written = []
    for arm, dataset_file in ARMS.items():
        for rkey, r in RECIPES.items():
            for seed in SEEDS:
                cfg = _cfg(r["head"], r["loss_function"], dataset_file, seed)
                # exp* prefix so run_all_experiments.py's glob picks it up.
                fname = f"exp_{arm}_{rkey}_s{seed:02d}.yaml"
                path = OUT_DIR / fname
                header = (
                    f"# Dataset-factor decomposition — arm={arm} recipe={rkey} "
                    f"seed={seed}\n"
                    f"# dataset={dataset_file}\n"
                    f"# 25 epochs, fp32 (use_amp=false), torch 2.12 + PIL "
                    f"(current env). 3-seed matched comparison rule.\n"
                    f"# Generated by scripts/generate_dataset_factor_configs.py "
                    f"— do not edit by hand.\n\n"
                )
                with open(path, "w", encoding="utf-8") as f:
                    f.write(header)
                    yaml.safe_dump(cfg, f, sort_keys=False, default_flow_style=False)
                written.append(fname)

    print(f"Wrote {len(written)} configs to {OUT_DIR}:")
    for w in sorted(written):
        print(f"  {w}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
